public void populateTable(){
        
        DefaultTableModel model = (DefaultTableModel) tblPersonData.getModel();
        model.setRowCount(0);
        
        for (Person a : personProfile.getPerson()) {
            
            Object[] row = new Object[4];
            row[0] = a;
            row[1] = a.getFName();
            row[2] = a.getLName();
            row[3] = String.valueOf(a.getAge());
            model.addRow(row);
        }
        
    }


